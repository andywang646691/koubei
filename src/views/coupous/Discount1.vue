<template lang="pug">
div
  router-view.nested-view(v-show="nestedView")
  .page-discount1(v-show="!nestedView")
    top-head
    .page-title  全场折扣券
    .cell-wrapper
      form-cell(
        title="券名称"
        v-model="coupouName"
      )
      mt-cell(
        is-link
        title="适用门店"
        value="请选择"
        v-on:click.native="$router.push({name: 'discount1.stores'})"
      )
    .cell-wrapper
      mt-cell(
        is-link
        title="活动开始时间"
        v-bind:value="startDate"
        v-on:click.native="openPicker('picker-start')"
      )
      mt-cell(
        is-link
        title="活动结束时间"
        v-bind:value="endDate"
        v-on:click.native="openPicker('picker-end')"
      )
      form-cell(
        title="品牌名称"
        v-model="brandName"
      )
    .cell-wrapper
      file-uploader(
        title="券LOGO"
      )
      mt-cell(
        is-link
        title="领取人群限制"
        v-bind:value="crowdType | formatCrowdType"
        v-on:click.native="sheetShwon1 = true"
      )
    .cell-wrapper
      form-cell(
        title="折扣力度"
        unit="折"
        maxlength="3"
        v-model="discount"
      )
      mt-cell(
        is-link
        title="券有效期"
        v-bind:value="coupouExpiredView"
        v-on:click.native="$router.push({name: 'discount1.coupouExpired'})"
      )
    div.btn-list
      button.btn.btn-reverse.btn-other(v-on:click="$router.push({name: 'discount1.other'})")
        span  其他设置
      button.btn.btn-green.btn-submit(v-on:click="submit")
        span 提交
    mt-datetime-picker(
      ref="picker-start"
      type="date"
      year-format="{value} 年"
      month-format="{value} 月"
      date-format="{value} 日"
      v-bind:value="pickerStartValue"
      v-on:confirm="pickerStartValue = arguments[0]"
    )
    mt-datetime-picker(
      ref="picker-end"
      type="date"
      year-format="{value} 年"
      month-format="{value} 月"
      date-format="{value} 日"
      v-bind:value="pickerEndValue"
      v-on:confirm="pickerEndValue = arguments[0]"
    )
    mt-actionsheet(
      :actions="peopleIsAllowed"
      v-model="sheetShwon1"
    )
</template>

<script>
import TopHead from '@/components/TopHead.vue'
import FormCell from '@/components/FormCell.vue'
import FileUploader from '@/components/FileUploader.vue'
import { format } from 'date-fns'
import { validateForm } from '@/services/utils'
import { discount1Validation } from './validation'
import { mapGetters, mapState } from 'vuex'
import { createCampaign } from '@/apis/index'
export default {
  name: 'discount1',
  components: {
    FileUploader,
    TopHead,
    FormCell
  },
  data () {
    return {
      coupouName: '',
      brandName: '',
      discount: '8.5',
      pickerStartValue: new Date(),
      pickerEndValue: new Date(),
      crowdType: 'ALL',
      sheetShwon1: false,
      peopleIsAllowed: [
        {
          name: '全体用户',
          method: () => { this.crowdType = 'ALL' }
        },
        {
          name: '大学生用户',
          method: () => { this.crowdType = 'STUDENT' }
        },
        {
          name: '新客专享',
          method: () => { this.crowdType = 'NEW_MEMBER_PROMO' }
        }
      ]
    }
  },
  computed: {
    ...mapGetters([
      'coupouExpiredView'
    ]),
    ...mapState('stores', [
      'shops'
    ]),
    ...mapState('discount1Other', [
      'other'
    ]),
    requestParams () {
      return {
        type: 'DIRECT_SEND',
        name: `${this.coupouName}${format(new Date(), 'MMDD')}`,
        startTime: `${this.startDate} 00:00:00`,
        endTime: `${this.endDate} 23:59:59`,
        autoDelayFlag: this.other.autoDelayFlag,
        constraintInfo: {
          userWinCount: this.other.userWinCount,
          userWinFrequency: this.other.userWinFrequency,
          crowdRestriction: this.crowdType,
          suitShops: this.shops,
          minCost: this.other.lowestLimit
        },
        promoTools: [
          {
            voucher: {},
            sendRule: {
              minCost: this.other.lowestLimit
            }
          }
        ],
        publishChannels: [
          {
            type: 'SHOP_DETAIL',
            name: '店铺页投放'
          },
          {
            type: 'SHORT_LINK',
            name: '短连接投放'
          }
        ]
      }
    },
    nestedView () {
      return this.$route.name !== 'discount1'
    },
    startDate () {
      return format(this.pickerStartValue, 'YYYY-MM-DD')
    },
    endDate () {
      return format(this.pickerEndValue, 'YYYY-MM-DD')
    }
  },
  methods: {
    submit () {
      // 需校验的字段
      let formData = {
        coupouName: this.coupouName,
        shops: this.shops,
        pickerStartValue: this.pickerStartValue,
        pickerEndValue: this.pickerEndValue,
        brandName: this.brandName,
        logo: this.logo,
        crowdType: this.crowdType,
        discount: this.discount,
        coupouExpired: this.coupouExpired
      }
      console.log(`result: ${validateForm(formData, discount1Validation)}`)
      if (validateForm(formData, discount1Validation)) return true
      createCampaign(formData)
    },
    openPicker (id) {
      this.$refs[id].open()
    }
  }
}
</script>

